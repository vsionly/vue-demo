    mounted() {
        this.items = document.querySelectorAll('.item-img')

        // 头部连线球的动画
        this.playBall()
        this.playLine()

        const carousel = document.querySelectorAll('.carousel');
        setInterval(() => {
            let offsetL = parseFloat(getComputedStyle(carousel[0]).left)
            // console.log(offsetL)
            if (offsetL === -938) offsetL = -3066
            carousel[0].style.left = offsetL + 1 + 'px'
            carousel[1].style.left = offsetL + 1 + 'px'
        }, 1000/30)

        const fastEl = document.querySelectorAll('.carousel-quick');
        setInterval(() => {
            let offsetL = parseFloat(getComputedStyle(fastEl[0]).left)
            // console.log(offsetL)
            if (offsetL === -938) offsetL = -3066
            fastEl[0].style.left = offsetL + 1 + 'px'
            fastEl[1].style.left = offsetL + 1 + 'px'
        }, 1000/60)
    },
    methods: {
        trackEvt (t) {
            window._hmt.push(['_trackEvent', '点击次数', t]);
        },
        playBall () {
            let balls = document.querySelectorAll('.animate-ball img')
            const Len = balls.length
            let ballInd = 0
            setInterval(() => {
                if (ballInd === 0) balls[Len-1].style.display = 'none'
                balls[ballInd].style.display = 'block'
                if (ballInd > 0) balls[ballInd-1].style.display = 'none'
                ballInd ++

                if (ballInd > Len-1) {
                    ballInd = 0
                }
            }, 40 )
        },
        playLine() {
            let balls = document.querySelectorAll(`.animate-line img`)
            let Len = balls.length
            let ballInd = 0
            setInterval(() => {
                balls[Len-1].style.zIndex = ballInd++
                Len --
                if (Len === 1) Len = balls.length
            }, 90 )
        },
        playAnimate(i) {
            //获取元素
            this.isEnd[i] = false
            clearInterval(this.timerOpp[i])
            clearInterval(this.times[i])
            let num = 1
            for (let ind = 0; ind < 25; ind++) {
                if (this.items[i].querySelectorAll('.animate-img')[ind + 1].style.display === 'block') {
                    this.items[i].querySelectorAll('.animate-img')[ind + 1].style.display = 'none';
                } else {
                    break;
                }
            }
            this.times[i] = setInterval(() => {
                console.log(1, num)
                if (this.isEnd[i]) {
                    this.items[i].querySelectorAll('.animate-img')[num-1].style.display = 'none'
                    num--
                } else {
                    this.items[i].querySelectorAll('.animate-img')[num-1].style.display = 'block'
                    num++
                }
                if (num > 25) {
                    this.isEnd[i] = true
                }
                if (num < 3 && this.isEnd[i]) {
                    this.isEnd[i] = false
                    clearInterval(this.times[i])
                }
            }, 40 )
        },
        playReverse(i) {
            clearInterval(this.times[i])
            clearInterval(this.timerOpp[i])

            let num = 26
            for (let ind = 25; ind > 1; ind--) {
                if (this.items[i].querySelectorAll('.animate-img')[num-1].style.display !== 'block') {
                    num --
                } else {
                    break;
                }
            }
            this.timerOpp[i] = setInterval(() => {
                console.log(2, num, this.items[i].querySelectorAll('.animate-img')[num-1].style.display)
                this.items[i].querySelectorAll('.animate-img')[num-1].style.display = 'none'
                num --
                if (num < 2) clearInterval(this.timerOpp[i])
            }, 40)
        }
    }
}
